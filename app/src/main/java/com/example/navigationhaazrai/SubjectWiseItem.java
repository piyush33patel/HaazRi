public class SubjectWiseItem {
    private  String mText1, mText2, mText3;
    public SubjectWiseItem(String text1, String text2, String text3)
    {
        mText1 = text1;
        mText2 = text2;
        mText3 = text3;
    }

    public String getmText1() {
        return mText1;
    }

    public String getmText2() {
        return mText2;
    }

    public String getmText3() {
        return mText3;
    }
}


